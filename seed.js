function weightedPick(weights) {
  const entries = Object.entries(weights);
  let total = 0; for (const [, w] of entries) total += w;
  let r = rnd() * total;
  for (const [k, w] of entries) { r -= w; if (r <= 0) return k; }
  return entries[0][0];
}

// 날짜: 2025-06-15 ~ 2026-06-12 사이, 최근일수록 살짝 더 많이
function randomDate() {
  const start = new Date('2025-06-15').getTime();
  const end = new Date('2026-06-12').getTime();
  const t = Math.pow(rnd(), 0.8); // 0.8제곱 -> 최근 쪽으로 약간 치우침
  const d = new Date(start + (end - start) * t);
  return d.toISOString().slice(0, 10);
}

